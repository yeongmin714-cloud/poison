def main():
    parser = argparse.ArgumentParser(
        description="Analyze synthetic training datasets for Neural Animation System"
    )
    parser.add_argument(
        "--dataset", type=str, default=None,
        help="Path to .npz dataset file (default: analyze all in output/)"
    )
    parser.add_argument(
        "--show", action="store_true",
        help="Display plots interactively"
    )
    parser.add_argument(
        "--no-save", action="store_true",
        help="Skip saving plots"
    )

    args = parser.parse_args()

    if args.dataset:
        # Analyze single dataset
        dataset_path = args.dataset
        if not os.path.exists(dataset_path):
            dataset_path = os.path.join(OUTPUT_DIR, args.dataset)
        if not os.path.exists(dataset_path):
            print(f"[Error] Dataset not found: {args.dataset}")
            sys.exit(1)

        analyze_dataset(
            dataset_path,
            show_plots=args.show,
            save_plots=not args.no_save,
        )
    else:
        # Analyze all datasets in output directory
        npz_files = [f for f in os.listdir(OUTPUT_DIR) if f.endswith(".npz")]
        if not npz_files:
            print(f"[Error] No .npz files found in {OUTPUT_DIR}")
            print("  Generate a dataset first:")
            print("    python synthetic_data_generator.py")
            sys.exit(1)

        for fname in sorted(npz_files):
            dataset_path = os.path.join(OUTPUT_DIR, fname)
            print(f"\n{'=' * 60}")
            analyze_dataset(
                dataset_path,
                show_plots=args.show,
                save_plots=not args.no_save,
            )

    print(f"\n{'=' * 60}")
    print(f"  Analysis complete. Plots: {PLOTS_DIR}/")
    print(f"{'=' * 60}")


if __name__ == "__main__":
    main()