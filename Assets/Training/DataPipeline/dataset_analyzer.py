def _plot_observation_correlation(obs: np.ndarray, avatar: str, prefix: str, show: bool):
    """Plot correlation matrix of observation channels."""
    # Subsample channels for readability (every 4th channel)
    step = max(1, obs.shape[1] // 30)
    obs_sub = obs[:, ::step]
    corr = np.corrcoef(obs_sub.T)

    fig, ax = plt.subplots(figsize=(10, 8))
    im = ax.imshow(corr, cmap="coolwarm", vmin=-1, vmax=1, interpolation="nearest")
    ax.set_title(f"Observation Channel Correlation (every {step}th dim) — {avatar.title()}")
    ax.set_xlabel("Channel Index")
    ax.set_ylabel("Channel Index")
    plt.colorbar(im, ax=ax, label="Correlation", shrink=0.8)

    plt.tight_layout()
    filepath = f"{prefix}_obs_correlation.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


def _plot_gait_distribution(
    gait_counts: Dict[str, int], avatar: str, prefix: str, show: bool
):
    """Plot distribution of gait types."""
    if not gait_counts:
        return

    fig, ax = plt.subplots(figsize=(10, 6))

    labels = list(gait_counts.keys())
    values = list(gait_counts.values())
    colors = [COLORS.get(g, "#999999") for g in labels]

    bars = ax.bar(labels, values, color=colors, alpha=0.8, edgecolor="white", linewidth=1.5)
    ax.set_title(f"Gait Type Distribution — {avatar.title()}", fontsize=13, fontweight="bold")
    ax.set_xlabel("Gait Type")
    ax.set_ylabel("Sample Count")
    ax.grid(True, alpha=0.3, axis="y")

    # Add value labels
    for bar, val in zip(bars, values):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + max(values) * 0.01,
                str(val), ha="center", va="bottom", fontsize=10)

    plt.tight_layout()
    filepath = f"{prefix}_gait_distribution.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


def _plot_obs_vs_act_correlation(
    obs: np.ndarray, acts: np.ndarray, avatar: str, prefix: str, show: bool
):
    """Plot correlation between observation and action components."""
    # Use first few velocity/action channels
    obs_vel = obs[:, :3]  # velocity
    act_root = acts[:, :3]  # root motion

    fig, axes = plt.subplots(1, 3, figsize=(15, 4))
    fig.suptitle(f"Observation vs Action — Velocity Components — {avatar.title()}", fontsize=13, fontweight="bold")

    labels = ["Forward (X)", "Vertical (Y)", "Lateral (Z)"]
    for i in range(3):
        ax = axes[i]
        ax.scatter(obs_vel[::10, i], act_root[::10, i], s=2, alpha=0.5,
                   color=COLORS.get(avatar, "blue"), edgecolors="none")
        ax.set_xlabel(f"Obs Velocity {labels[i]}")
        ax.set_ylabel(f"Act Root Motion {labels[i]}")
        ax.grid(True, alpha=0.3)
        # Line of best fit
        try:
            from numpy.polynomial import polynomial as P
            x = obs_vel[::10, i]
            y = act_root[::10, i]
            mask = ~np.isnan(x) & ~np.isnan(y)
            if np.sum(mask) > 10:
                c, stats = P.polyfit(x[mask], y[mask], 1, full=True)
                x_fit = np.linspace(x[mask].min(), x[mask].max(), 100)
                ax.plot(x_fit, P.polyval(x_fit, c), "r--", linewidth=1.5, alpha=0.8)
        except Exception:
            pass

    plt.tight_layout()
    filepath = f"{prefix}_obs_act_correlation.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


def _plot_gait_patterns(
    obs: np.ndarray, gait_types: List[str], layout: Dict,
    avatar: str, prefix: str, show: bool
):
    """Plot velocity traces for different gait types."""
    if not isinstance(gait_types, list) or len(gait_types) < 10:
        return

    fig, axes = plt.subplots(len(GAIT_TYPES), 1, figsize=(14, 3 * len(GAIT_TYPES)), sharex=True)
    fig.suptitle(f"Velocity Profiles by Gait Type — {avatar.title()}", fontsize=14, fontweight="bold")

    vel_start, vel_end = layout.get("velocity", (0, 3))
    # Ensure both are ints
    if not isinstance(vel_start, int):
        vel_start, vel_end = 0, 3

    for idx, gait in enumerate(GAIT_TYPES):
        ax = axes[idx] if len(GAIT_TYPES) > 1 else axes
        mask = [g == gait for g in gait_types]
        if sum(mask) < 5:
            ax.text(0.5, 0.5, f"No samples for '{gait}'", ha="center", va="center",
                    transform=ax.transAxes, fontsize=10, style="italic")
            ax.set_ylabel(gait.title())
            continue

        # Take first 200 samples of this gait
        gait_indices = np.where(mask)[0][:200]
        vel_samples = obs[gait_indices, vel_start:vel_end]

        for i, label in enumerate(["Forward", "Vertical", "Lateral"]):
            ax.plot(vel_samples[:, i], label=label, alpha=0.8, linewidth=1.0)
        ax.set_ylabel(gait.title())
        ax.grid(True, alpha=0.3)
        if idx == 0:
            ax.legend(fontsize=8, loc="upper right")
        if idx == len(GAIT_TYPES) - 1:
            ax.set_xlabel("Frame Index")

    plt.tight_layout()
    filepath = f"{prefix}_gait_patterns.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


