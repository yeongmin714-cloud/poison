def _plot_observation_channels(obs: np.ndarray, avatar: str, prefix: str, show: bool):
    """Plot individual observation channel values as a heatmap."""
    # Take a subset for visualization
    N_show = min(500, obs.shape[0])
    obs_subset = obs[:N_show, :80]  # first 80 channels for readability

    fig, ax = plt.subplots(figsize=(16, 6))
    im = ax.imshow(obs_subset.T, aspect="auto", cmap="RdBu_r", interpolation="nearest",
                   vmin=-1, vmax=1)
    ax.set_title(f"Observation Channels (first 80 dims, {N_show} samples) — {avatar.title()}")
    ax.set_xlabel("Sample Index")
    ax.set_ylabel("Channel Index")
    plt.colorbar(im, ax=ax, label="Value", shrink=0.8)

    plt.tight_layout()
    filepath = f"{prefix}_obs_heatmap.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


