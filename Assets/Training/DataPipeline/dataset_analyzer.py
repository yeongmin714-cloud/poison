def _plot_observation_distribution(
    obs: np.ndarray, layout: Dict, avatar: str, prefix: str, show: bool
):
    """Plot the distribution of all observation values."""
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle(f"Observation Distribution — {avatar.title()}", fontsize=14, fontweight="bold")

    # Histogram of all values
    axes[0, 0].hist(obs.flatten(), bins=100, color=COLORS.get(avatar, "blue"), alpha=0.7, density=True)
    axes[0, 0].set_title("All Observation Values")
    axes[0, 0].set_xlabel("Value")
    axes[0, 0].set_ylabel("Density")
    axes[0, 0].grid(True, alpha=0.3)

    # Per-channel mean
    channel_means = np.mean(obs, axis=0)
    axes[0, 1].plot(channel_means, color=COLORS.get(avatar, "blue"), linewidth=0.8)
    axes[0, 1].set_title("Per-Channel Mean")
    axes[0, 1].set_xlabel("Channel Index")
    axes[0, 1].set_ylabel("Mean Value")
    axes[0, 1].grid(True, alpha=0.3)

    # Per-channel std
    channel_stds = np.std(obs, axis=0)
    axes[1, 0].plot(channel_stds, color=COLORS.get(avatar, "blue"), linewidth=0.8)
    axes[1, 0].set_title("Per-Channel Standard Deviation")
    axes[1, 0].set_xlabel("Channel Index")
    axes[1, 0].set_ylabel("Std Dev")
    axes[1, 0].grid(True, alpha=0.3)

    # Component-wise mean comparison
    component_labels = []
    component_means = []
    for comp_name, comp_range in layout.items():
        if comp_name == "total":
            continue
        if not isinstance(comp_range, (list, tuple)) or len(comp_range) != 2:
            continue
        start, end = comp_range
        if end is None:
            continue
        component_labels.append(comp_name.replace("_", " ").title())
        component_means.append(np.mean(obs[:, start:end]))

    if component_labels:
        axes[1, 1].barh(component_labels, component_means, color=COLORS.get(avatar, "blue"), alpha=0.7)
        axes[1, 1].set_title("Component-wise Mean")
        axes[1, 1].set_xlabel("Mean Value")
        axes[1, 1].grid(True, alpha=0.3, axis="x")

    plt.tight_layout()
    filepath = f"{prefix}_obs_distribution.png"
    plt.savefig(filepath, dpi=150, bbox_inches="tight")
    if show:
        plt.show()
    plt.close()
    print(f"    Saved: {os.path.basename(filepath)}")


