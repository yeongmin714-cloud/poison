def analyze_dataset(
    dataset_path: str,
    show_plots: bool = False,
    save_plots: bool = True,
    verbose: bool = True,
) -> Dict:
    """
    Analyze a dataset and generate visualizations.

    Args:
        dataset_path: Path to .npz dataset file
        show_plots: Display plots interactively
        save_plots: Save plots to PLOTS_DIR
        verbose: Print analysis results

    Returns:
        dict of analysis results
    """
    if verbose:
        print(f"[Analyzer] Loading dataset: {dataset_path}")
        print("─" * 50)

    # Load data
    data = load_dataset(dataset_path)
    obs = data["observations"]
    acts = data["actions"]
    meta = data["metadata"]

    N, obs_dim = obs.shape
    act_dim = acts.shape[1]

    # Determine avatar type
    avatar_type = meta.get("avatar_type", "biped").lower()
    config = AVATAR_CONFIGS.get(avatar_type, AVATAR_CONFIGS["biped"])
    joint_count = meta.get("joint_count", config["joint_count"])
    obs_layout = _compute_obs_layout(joint_count)
    act_layout = _compute_act_layout(joint_count)

    # Extract gait types
    gait_types = meta.get("gait_type", ["unknown"] * N)
    if isinstance(gait_types, str):
        try:
            gait_types = json.loads(gait_types)
        except (json.JSONDecodeError, TypeError):
            gait_types = ["unknown"] * N

    # Compute statistics
    stats = compute_statistics(data)

    # --- Analysis: Component Distributions ---
    analysis = {
        "dataset_path": dataset_path,
        "num_samples": N,
        "obs_dim": obs_dim,
        "act_dim": act_dim,
        "avatar_type": avatar_type,
        "joint_count": joint_count,
        "stats": stats,
        "gait_counts": {},
    }

    if gait_types and isinstance(gait_types, list):
        from collections import Counter
        gait_counts = Counter(gait_types)
        analysis["gait_counts"] = dict(gait_counts)

    if verbose:
        print(f"  Samples:     {N}")
        print(f"  Avatar:      {avatar_type}")
        print(f"  Obs dim:     {obs_dim}")
        print(f"  Act dim:     {act_dim}")
        print(f"  Joint count: {joint_count}")
        print(f"  Obs range:   [{stats['obs_min']:.4f}, {stats['obs_max']:.4f}]")
        print(f"  Act range:   [{stats['act_min']:.4f}, {stats['act_max']:.4f}]")
        if analysis["gait_counts"]:
            print(f"  Gait dist:   {analysis['gait_counts']}")

    # --- Generate Plots ---
    if save_plots or show_plots:
        base_name = os.path.splitext(os.path.basename(dataset_path))[0]
        plot_prefix = os.path.join(PLOTS_DIR, base_name)

        _plot_observation_distribution(obs, obs_layout, avatar_type, plot_prefix, show_plots)
        _plot_action_distribution(acts, act_layout, avatar_type, plot_prefix, show_plots)
        _plot_observation_channels(obs, avatar_type, plot_prefix, show_plots)
        _plot_action_channels(acts, avatar_type, plot_prefix, show_plots)
        _plot_observation_correlation(obs, avatar_type, plot_prefix, show_plots)
        _plot_gait_distribution(analysis["gait_counts"], avatar_type, plot_prefix, show_plots)
        _plot_obs_vs_act_correlation(obs, acts, avatar_type, plot_prefix, show_plots)
        _plot_gait_patterns(obs, gait_types, obs_layout, avatar_type, plot_prefix, show_plots)

        if verbose:
            print(f"\n  Plots saved to: {PLOTS_DIR}/")

    return analysis


