using UnityEngine;
using System.Collections.Generic;

public class UICore : MonoBehaviour
{
    public static UICore Instance { get; private set; }
    
    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}